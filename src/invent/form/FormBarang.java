    public FormBarang() {
        initComponents();
         model = new TableModelBarang();
        jTable1.setModel(model);
        barangg = new ArrayList<Barang>();
        loadData();
        
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
        public void valueChanged(ListSelectionEvent lse) {
                int row = jTable1.getSelectedRow();
                if (row !=-1){
                    barang = model.get(row);
                    txtNama.setText(barang.getNama());
                    txtSku.setText(barang.getSku());
                    txtStok.setText(String.valueOf(barang.getStok()));
                    txtSku.setEnabled(false);
                    if (barang.getGambar()!=null){
                        try {
                            ObjectInputStream inputStream = new ObjectInputStream(new ByteArrayInputStream(model.get(row).getGambar()));
                            ImageIcon icon  =  (ImageIcon) inputStream.readObject();
                            image = icon.getImage();
                            panelGambar1.setImage(image);
                            inputStream.close();
                        } catch (ClassNotFoundException ex) {
                            Logger.getLogger(FormBarang.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (IOException ex) {
                            Logger.getLogger(FormBarang.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                }
                btnUbah.setEnabled(true);
                btnHapus.setEnabled(true);
                btnSimpan.setEnabled(false);
            }
        });        
    }
    
