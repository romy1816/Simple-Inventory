    private class TableModelBarang extends AbstractTableModel{
        private List<Barang> list = new ArrayList();

        public void setData(List<Barang> list){
            this.list = list;
            fireTableDataChanged();
        }
        public int getRowCount() {
            return list.size();
        }

        public int getColumnCount() {
            return 3;
        }

        public Object getValueAt(int rowIndex, int columnIndex) {
            switch(columnIndex){
                case 0 : return list.get(rowIndex).getNama();
                case 1 : return list.get(rowIndex).getSku();
                case 2 : return list.get(rowIndex).getStok();
                default: return null;
            }
        }

        @Override
        public String getColumnName(int columnIndex) {
            switch(columnIndex){
                case 0 : return "Nama";
                case 1 : return "SKU";
                case 2 : return "Stok";
                default: return null;
            }
        }
        public void insertTable(Barang barang){
            list.add(barang);
            fireTableDataChanged();
        }
        public void updateTable(Barang barang,int rowIndex){
            list.set(rowIndex, barang);
            fireTableDataChanged();
        }
        public Barang get(int rowIndex){
            return list.get(rowIndex);
        }
    }
    
